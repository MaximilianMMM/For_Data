#include <stdio.h>

int week() {
    int day, month, year;

    printf("Введіть дату (день, місяць, рік): ");
    scanf("%d %d %d", &day, &month, &year);

    // Перевірка валідності дати
    if (month < 1 || month > 12 || day < 1 || day > 31) {
        printf("Некоректна дата!\n");
        return 1;
    }

    // Масив з кількістю днів у кожному місяці
    int days_per_month[] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};

    // Перевірка на високосний рік
    if ((year % 4 == 0 && year % 100 != 0) || year % 400 == 0)
        days_per_month[1] = 29;

    // Обчислення дня тижня за алгоритмом Зеллера
    int a = (14 - month) / 12;
    int y = year - a;
    int m = month + 12 * a - 2;
    int weekday = (day + y + y / 4 - y / 100 + y / 400 + (31 * m) / 12) % 7;

    // Визначення дня тижня (0 - Неділя, 1 - Понеділок, ..., 6 - Субота)
    char* weekdays[] = {"Неділя", "Понеділок", "Вівторок", "Середа", "Четвер", "П'ятниця", "Субота"};

    // Виведення результату
    printf("День тижня: %s\n", weekdays[weekday]);

    return 0;
}
